    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        // Lấy phiên session
        HttpSession session = request.getSession();

        // acc : nhận giá trị account của session
        Account acc = (Account) session.getAttribute("account");

        //Khai báo một biến c kiểu DAO , và gán mẫu Singleton
        DAO c = DAO.INSTANCE;

        //Gọi phương thức getBought trong lớp DAO
        ArrayList<Bought> listbought = c.getBought(acc.getUserid());
        request.setAttribute("list", listbought);
        request.getRequestDispatcher("Views/bought.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int idbought = Integer.parseInt(request.getParameter("id"));
        int process = Integer.parseInt(request.getParameter("process"));
        DAO c = DAO.INSTANCE;
        if (process != 3) {
            doGet(request, response);
        } else {
            c.changeBought(idbought, process + 1);
        }
        doGet(request, response);
    }
